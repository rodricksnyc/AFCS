                      <p class="small">The history of Wright-Patterson as a military installation predates World War I, and its general aviation history dates back even further. In 1904 and 1905, aviation pioneers and Ohio natives Orville and Wilbur Wright tested their Wright Flyer aircraft on the site now known as Huffman Prairie Flying Field.</p>

