                        <h1 class="acq two">Life at Arnold</h1>

                        <p class="small">Located in Coffee and Franklin counties in Tennessee, Arnold Air Force Base is the perfect place to enjoy the scenic beauty that surrounds the nearby cities of Manchester, Tullahoma and Winchester. Right between Nashville and the busy city of Chattanooga, AEDC is a great place for both work and play.</p>

                        <p class="small">AEDC is the only active duty Air Force base in Tennessee, one of the largest employers in the area, and surrounded by breathtaking mountains and national parks. Catch a movie on the weekend, hike at a nearby trail or take classes at Arnold’s Fitness Center. Travel about an hour outside the base and you will find a number of ski resorts, hiking trails and higher education institutions.</p>

