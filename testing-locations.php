                        <p class="small">Aircraft charged with the maritime challenge included the A-10, B-1, B-52, F-15 and F-16. Depending on their platform, fighters and bombers were armed with Sniper and LITENING Advanced Targeting pods and various combinations of munitions such laser-guided bombs, Joint Direct Attack Munitions, Maverick air-to-ground missiles, cluster bombs and 20mm and 30mm high-explosive incendiary ammunition.</p>

