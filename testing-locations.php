                        <h1 class="acq two">Innovations at Elgin</h1>

                        <p class="small strong">A-10 Thunderbolt</p>

                        <p class="small">The 40th Flight Test Squadron at Eglin recently made headlines when an A-10 Thunderbolt II fired a guided rocket that impacted only inches away from its intended target. The 2.75 diameter, 35-pound, laser-guided rocket is known as the fixed-wing Advanced Precision Kill Weapon System II (APKWS).</p>

                        <p class="small">The test squadron performed three sorties to demonstrate the capability and ensure the rocket could be fired safely from a fixed wing aircraft, a test that had never been accomplished before.</p>

                        <p class="small">The first sortie tested whether aircraft flight would be impacted by carrying the rocket and launcher. During the second sortie, the A-10 fired an unguided inert rocket to ensure the weapon would separate from the aircraft without any issues. For the final sortie, two armed, guided rockets were fired at a surface target at altitudes of 10,000 and 15,000 feet. The last APKWS shot was fired into a 70-knot headwind and impacted the target within the two-meter requirement specifications.</p>

                        <p class="small">Both shots were considered successful, and the APKWS has numerous potential benefits to the warfighter, including more precise targeting and accuracy.</p>


