                        <h1 class="acq two">Innovations at Arnold</h1>

                        <p class="small strong">The Space Threat Assessment Testbed (STAT)</p>

                        <p class="small">The Space Threat Assessment Testbed (STAT) at Arnold Engineering Development Complex (AEDC) is a new, one-of-a-kind testing facility designed to simulate a realistic space environment using multiple source simulators to emulate the conditions existing at various orbits. The STAT facility comes complete with a fully-instrumented micro-satellite available to use as a test article in the facility during the initial and final checkout for full mission readiness. This unique space asset will be able to provide a better understanding of the natural or man-made enemy forces that threaten U.S. satellite capabilities and will fulfill a long-standing need for a national mission-critical asset with capabilities previously unavailable anywhere in the world. Next-Generation Heat Shield Materials for Missiles</p>

                        <p class="small">As a submarine-launched ballistic missile (SLBM) leaves the U.S. Navy Trident II submarine and is clear of the water on a rapid-fire trajectory to destroy its target, Navy fleet commanders must know for sure if it can survive the elements. AEDC Arc-Heated facilities are unique in their ability to simulate heat flux and pressure conditions typical of long-range ballistic missile re-entry through the atmosphere. Since 2000, the U.S. Navy has come to AEDC to conduct aero-thermal testing. AEDC’s High-Enthalpy Arc-Heated facilities provide the DOD with the capabilities needed when developing high-speed missiles and vehicles.</p>

                        <p class="small strong">J-6 Large Rocket Motor Testing Facility</p>

                        <p class="small">AEDC's J-6 Large Rocket Motor Testing Facility is one of the few outfits in the world sophisticated enough to test a rocket motor like the CASTOR® 30XL.</p>

                        <p class="small">How does a rocket motor testing facility accurately assess the performance of a motor designed to deliver cargo to the International Space Station? The testing chambers at the J-6 Large Rocket Motor Testing Facility are specially designed to handle the amount of exhaust products during the long static fire duration and simulate upper atmospheric conditions, and are strong enough to measure the thrust, pressure burn rate and stresses of firing during takeoff.</p>


                        <p class="small">The J-6 facility provides ground test simulations for solid-propellant rocket motors over a wide range of simulated pressure altitudes. AEDC has unique test capabilities for testing rocket propulsion systems with high performance/high area-ratio nozzles, and those requiring altitude start and restart, stage separation and spin testing.</p>

                        <p class="small">These facilities are the largest of their kind in the world and provide the only altitude test capability for medium to large and rocket propulsion systems in the United States</p>

