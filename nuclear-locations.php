                      <p class="small strong">Outdoor Activities</p>

                      <p class="small">Spending time in nature is a great way to recharge from the demands of work. Albuquerque is ideal for outdoor activities with mountains, rivers, deserts and a scenic landscape for hiking, camping, fishing, biking, skiing and climbing. Enjoying the outdoors is a hallmark of life for AFNWC employees.</p>
                      <p class="small strong">Petroglyph National Monument</p>
                      <p class="small">You can't help but notice the peaks that dominate the western horizon. Those peaks are actually the Petroglyph National Monument, 17 miles of dormant volcanic sites and more than 20,000 ancestral Pueblo and early Spanish settler carvings along Albuquerque's West Mesa. The monument is cooperatively managed by the National Park Service and the City of Albuquerque Open Space Division, and is intended for cultural visitors and recreational use.</p>

