                      <p class="small strong">We’ve Made History:</p>
                      <p class="small">AFNWC, Air Force Materiel Command's center of excellence for nuclear weapon systems, was activated at Kirtland Air Force Base in New Mexico, in 2006. Prior to that, the base has had a long history of weapons development and sustainment: 1939: World War II broke out, and Kirtland Air Force Base began as Albuquerque Army Air Base, an Army Air Corps training field. Thousands of New Mexico residents, working under the Great Depression-era Works Progress Administration, helped build the base.</p>
                      <ul class="whiteList">
                        <li class="whiteList">1941: The first troops arrived to train and the first aircraft, a lone B-18 bomber, landed on the north-south runway.</li>
                        <li class="whiteList">1942: The new airfield began to boom with the arrival of the pilots, bombardiers, navigators and other crew members.</li>
                        <li class="whiteList">1943-1944: The base's three schools, Advanced Flying, Bombardier Training and the Multi-Engine Transition schools were operating at full capacity.</li>

                        <li class="whiteList">1946: Kirtland was assigned to the Air Force Materiel Command, and began focusing on flight test activities for the Manhattan Engineering District, the wartime organization that produced the first atomic bombs.</li>
                        <li class="whiteList">1947: Kirtland Field’s mission was established by AMC as the USAAF nuclear weapons facility, a continuation of the wartime <a class="whiteStrong" href="https://en.wikipedia.org/wiki/509th_Composite_Group" target="_blank">Wendover Army Air Base (509th Composite Group</a> – <a class="whiteStrong" href="https://en.wikipedia.org/wiki/Manhattan_Project" target="_blank">Manhattan Project</a> operations. Kirtland's role in the testing and evaluation of atomic weapons increased in 1947 when Kirtland Army Air Field, became Kirtland Air Force Base.</li>

                        <li class="whiteList">1949: Renamed Kirtland Air Force Base, the air field became headquarters for the newly created Special Weapons Command at Kirtland Air Force Base following World War II, to determine employment of emerging weapon systems.</li>
                        <li class="whiteList">1952-1955: Renamed the Air Force Special Weapons Center, the airfield participated in atmospheric nuclear tests, in which biophysicists deliberately flew through nuclear clouds to determine radiation hazards. In other tests, engineers launched sounding rockets to study the effects of high-altitude explosions and the nature of the recently discovered Van Allen radiation belts.</li>

                        <li class="whiteList">1958: Scientists at the Air Force Special Weapons Center began to simulate the effects of explosions in order to strengthen missiles, missile sites and aircraft against possible attack.</li>

                        <li class="whiteList">1971: Kirtland merged with the Manzano and Sandia bases to create the large military complex that is now Kirtland Air Force Base.</li>
                        <li class="whiteList">1976: The Air Force Special Weapons Center was inactivated, and the Center’s weapons-related missions transferred to the Air Force Weapons Laboratory.</li>

                        <li class="whiteList">1982: The Air Force Space Technology Center was activated at Kirtland.</li>
                        <li class="whiteList">2006: Weapons-related missions were transferred to what is now AFNWC.</li>

                      </ul>
